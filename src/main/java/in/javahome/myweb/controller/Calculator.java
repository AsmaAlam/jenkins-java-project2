package in.javahome.myweb.controller;
/*
 * 
 */
public class Calculator {
	
	/*
	 * @param i
	 * @param j
  	 * @param k
	 * @return int
	 */
	public int add(int i, int j, int k){
		return i+j+k;
	}
	public int multiply(int i, int j, int k){
		return i*j*k;
	}
	
	public int sub(int i, int j, int k){
		return i-j-k;
	}
}
